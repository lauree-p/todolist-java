
public class Point {

	int num;
	float x;
	float y;
	
	
	public Point() {
	}
	
	public Point(int num, float x, float y) {
		super();
		this.num = num;
		this.x = x;
		this.y = y;
	}
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	
	
	
}
